package org.tnsif.thiskeyword;

public class ThisKeywordDemo {
        int a;
        int b;
       public void setdata(int a,int b)
       {
    	  this.a=a;
    	  this.b=b;
       }
       public void Showdata()
       {
    	   System.out.println(" value of a:"+a);
    	   System.out.println(" value of b:"+b);
       }
      
        
}
